package Java_Basic.com;

public class WhileLoop {

	public static void main(String[] args) {
		
		
		int a=10;
		//while loop
		while(a>=1){
			//statement
			System.out.println(a);
			a--;
		}
		System.out.println("do loop");
		
		
		// do 
		
		//  do{ //code to be executed / loop body //update statement }
		//	  while (condition);
		//  }
		   int k=1;
		   do {
			   System.out.println(k);
			   k++;

		   }while(k<=5);
	}

}
